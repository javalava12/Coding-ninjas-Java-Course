import java.util.Scanner;
public class Star_Pattern {
    public static void main(String[] args) {
        Scanner s =new Scanner(System.in);
        int n=s.nextInt();
        int i=0;
        int val=1;
        while(i<n){
            int space=1;
            while(space<n-i){
                System.out.print(" ");
                space++;
            }
            int j=0;
            while(j<val){
                System.out.print("*");
                j++;
            }
            val+=2;
            System.out.println();
            i++;
        }
    }
}
